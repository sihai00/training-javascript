/*
* 目标：二进制转字符
* 解释：传入二进制字符串，翻译成英语句子并返回
* 例如：binaryAgent("01001001 00100000 01101100 01101111 01110110 01100101 00100000 01000110 01110010 01100101 01100101 01000011 01101111 01100100 01100101 01000011 01100001 01101101 01110000 00100001") 应该返回 "I love FreeCodeCamp!"
* 
* */

function binaryAgent(str) {
  if (typeof str !== 'string') return 

  return str.split(' ').map(v => {
    return String.fromCharCode(parseInt(`${v}`, 2))
  }).join('')
}

